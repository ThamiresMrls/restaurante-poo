    public void populaTabela(String busca) throws SQLException{

        IngredienteDao fd = new IngredienteDao();
        ingredientes = fd.getLista("%"+busca+"%");
        
        for (Ingrediente i1 : ingredientes) {
            insereTabela(i1);
        }
    }

    public void insereTabela(Ingrediente i1){

        tmIngrediente.addRow(new String[]{
            String.valueOf(i1.getId()),
            i1.getNome(),
            i1.getMarca(),
            String.valueOf(i1.getEstoque()),
        });
    }





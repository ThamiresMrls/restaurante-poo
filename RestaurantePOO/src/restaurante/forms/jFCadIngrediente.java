    private void escolherLinha(){

        int linha = jTIngrediente.getSelectedRow();
        System.out.println(linha);

        Ingrediente i1 = new Ingrediente();
        String temp;

        temp = String.valueOf(jTIngrediente.getValueAt(linha, 0));
        i1.setId(Integer.parseInt(temp));
        i1.setNome(String.valueOf(jTIngrediente.getValueAt(linha,1)));
        i1.setMarca(String.valueOf(jTIngrediente.getValueAt(linha,2)));
        temp = String.valueOf(jTIngrediente.getValueAt(linha, 3));
        i1.setEstoque(Integer.parseInt(temp));

        escreverJTextField(i1);
        
    }

    private void escreverJTextField(Ingrediente i1){

        System.out.println(i1);

        jTID.setEditable(true);
        jTID.setText(String.valueOf(i1.getId()));
        jTID.setEditable(false);

        jTNome.setText(i1.getNome());
        jTMarca.setText(i1.getMarca());
        jTEstoque.setText(String.valueOf(i1.getEstoque()));

    }
    
    private void novoCadastro(){

        Ingrediente i1 = new Ingrediente();
        i1.setNome(jTNome.getText());
        i1.setMarca(jTMarca.getText());
        i1.setEstoque(Integer.parseInt(jTEstoque.getText()));

         try {
            IngredienteDao dao = new IngredienteDao();
            dao.adiciona(i1);

            tmIngrediente.setNumRows(0);
            populaTabela("");

        } catch (SQLException ex) {
            Logger.getLogger(jFCadIngrediente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void alteraCadastro(){

        Ingrediente f1 = new Ingrediente();
        f1.setId(Integer.parseInt(jTID.getText()));
        f1.setNome(jTNome.getText());
        f1.setMarca(jTMarca.getText());
        f1.setEstoque(Integer.parseInt(jTEstoque.getText()));

        try {
            IngredienteDao dao = new IngredienteDao();
            dao.altera(f1);
            tmIngrediente.setNumRows(0);
            populaTabela("");
        } catch (SQLException ex) {
            Logger.getLogger(jFCadIngrediente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void removerCadastro(){
        
        Ingrediente i1 = new Ingrediente();
        i1.setId(Integer.parseInt(jTID.getText()));
        
        IngredienteDao dao;
        try {
            dao = new IngredienteDao();
            dao.remove(i1);
            tmIngrediente.setNumRows(0);
            populaTabela("");
        } catch (SQLException ex) {
            Logger.getLogger(jFCadIngrediente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


    public void populaTabela(String busca) throws SQLException{

        FornecedorDao fd = new FornecedorDao();
        fornecedores = fd.getLista("%"+busca+"%");
        
        for (Fornecedor f1 : fornecedores) {
            insereTabela(f1);
        }
    }

    public void insereTabela(Fornecedor f1){

        tmFornecedor.addRow(new String[]{
            String.valueOf(f1.getId()),
            f1.getCnpj(),
            f1.getNome(),
            f1.getEndereco(),
            f1.getTelefone(),
            f1.getEmail(),
        });
    }





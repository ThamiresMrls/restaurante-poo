    private void escolherLinha(){

        int linha = jTFornecedor.getSelectedRow();

        Fornecedor f1 = new Fornecedor();
        String temp;

            temp = String.valueOf(jTFornecedor.getValueAt(linha, 0));
        f1.setId(Integer.parseInt(temp));
        f1.setCnpj(String.valueOf(jTFornecedor.getValueAt(linha, 1)));
        f1.setNome(String.valueOf(jTFornecedor.getValueAt(linha, 2)));
        f1.setEndereco(String.valueOf(jTFornecedor.getValueAt(linha, 3)));
        f1.setTelefone(String.valueOf(jTFornecedor.getValueAt(linha, 4)));
        f1.setEmail(String.valueOf(jTFornecedor.getValueAt(linha, 5)));

        escreverJTextField(f1);
        
    }

    private void escreverJTextField(Fornecedor f1){

        jTID.setEditable(true);
        jTID.setText(String.valueOf(f1.getId()));
        jTID.setEditable(false);

        jTNome.setText(f1.getNome());
        jTEndereco.setText(f1.getEndereco());
        jTCnpj.setText(f1.getCnpj());
        jTTelefone.setText(f1.getTelefone());
        jTEmail.setText(f1.getEmail());
    }
    
    private void novoCadastro(){

        Fornecedor f1 = new Fornecedor();
        f1.setNome(jTNome.getText());
        f1.setEndereco(jTEndereco.getText());
        f1.setCnpj(jTCnpj.getText());
        f1.setTelefone(jTTelefone.getText());
        f1.setEmail(jTEmail.getText());


         try {
            FornecedorDao dao = new FornecedorDao();
            dao.adiciona(f1);

            tmFornecedor.setNumRows(0);
            populaTabela("");

        } catch (SQLException ex) {
            Logger.getLogger(jFCadFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void alteraCadastro(){

        Fornecedor f1 = new Fornecedor();
        f1.setId(Integer.parseInt(jTID.getText()));
        f1.setNome(jTNome.getText());
        f1.setCnpj(jTCnpj.getText());
        f1.setEndereco(jTEndereco.getText());
        f1.setTelefone(jTTelefone.getText());
        f1.setEmail(jTEmail.getText());

        try {
            FornecedorDao dao = new FornecedorDao();
            dao.altera(f1);
            tmFornecedor.setNumRows(0);
            populaTabela("");
        } catch (SQLException ex) {
            Logger.getLogger(jFCadFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void removerCadastro(){
        
        Fornecedor f1 = new Fornecedor();
        f1.setId(Integer.parseInt(jTID.getText()));
        
        try {
            FornecedorDao dao = new FornecedorDao();
            dao.remove(f1);
            tmFornecedor.setNumRows(0);
            populaTabela("");
        } catch (SQLException ex) {
            Logger.getLogger(jFCadFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

